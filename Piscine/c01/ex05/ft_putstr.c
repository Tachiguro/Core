#include <unistd.h>

void	ft_putstr(char *str)
{
	int	counter;

	counter = 0;
	while (str[counter] != '\0')
	{
		write(1, &str[counter], 1);
		counter++;
	}
}

#include <stdio.h>
int	main(void)
{
	char	str[] = "This is a test.";
	ft_putstr(str);
}
